#include <iostream>

using namespace std;

int main(){

  int x=3, y=4;
  cout << "the sum of "<< x << " and " << y << endl;
  cout << x + y << endl;
  cout << "the difference of "<< x << " and " << y << endl;
  cout << x - y << endl;
  cout << "the product of "<< x << " and " << y << endl;
  cout << x * y << endl;
  cout << "the quotient of "<< x << " and " << y << endl;
  cout << x / y << endl;
  cout << "the remainder of "<< x << " and " << y << endl;
  cout << x % y << endl;
  cout << "the increment of "<< x << " is " << x++ << endl;
  cout << "the decrement of "<< y << " in modulo 3 is " << (y--) % 3 << endl;
  return 0;
}
